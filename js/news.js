function showNews() {
    const container = document.getElementById('newsContainer');
    if (!container) return;
    container.innerHTML = '';

    for (let i = 0; i < newsData.length; i++) {
        const item = newsData[i];
        const card = document.createElement('div');
        card.className = 'news-card';

        let imageHtml = '';
        if (item.image && item.image !== '') {
            imageHtml = `<img class="news-image" src="${item.image}" alt="News image">`;
        }

        card.innerHTML = `
            ${imageHtml}
            <div class="news-date">${item.date}</div>
            <div class="news-title">${item.title}</div>
            <div class="news-full">${item.full}</div>
            <button class="news-btn" onclick="toggleNews(this)">Показать полностью</button>
        `;
        container.appendChild(card);
    }
}

function toggleNews(button) {
    const card = button.closest('.news-card');
    card.classList.toggle('open');
    
    if (card.classList.contains('open')) {
        button.textContent = 'Скрыть';
    } else {
        button.textContent = 'Показать полностью';
    }
}